import { createHash } from 'node:crypto'
import { mkdir, readdir, unlink, writeFile } from 'node:fs/promises'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import sharp from 'sharp'
import { materialPalette } from './material-palette.mjs'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const outputDir = join(root, 'public/materials')
const generatedPath = join(root, 'src/data/materials.generated.ts')
const tileSize = 48
const generator = { kind: 'original-procedural', version: 1, tileSize }

const clamp = (value) => Math.max(0, Math.min(255, Math.round(value)))
const parseHex = (hex) => [
  Number.parseInt(hex.slice(1, 3), 16),
  Number.parseInt(hex.slice(3, 5), 16),
  Number.parseInt(hex.slice(5, 7), 16),
]
const seedFor = (id) => createHash('sha256').update(`floor-portrait:${id}:v1`).digest().readUInt32LE(0)

function unitNoise(seed, x, y, channel = 0) {
  let value = seed ^ Math.imul(x + 0x9e3779b9, 0x85ebca6b) ^ Math.imul(y + channel * 97, 0xc2b2ae35)
  value ^= value >>> 16
  value = Math.imul(value, 0x7feb352d)
  value ^= value >>> 15
  value = Math.imul(value, 0x846ca68b)
  value ^= value >>> 16
  return (value >>> 0) / 0xffffffff
}

function textureAdjustment(category, seed, x, y) {
  const grain = (unitNoise(seed, x, y) - 0.5) * 10

  if (category === 'Stone') {
    const cellX = Math.floor((x + (Math.floor(y / 7) % 2) * 3) / 7)
    const cellY = Math.floor(y / 7)
    const coarse = (unitNoise(seed, cellX, cellY, 1) - 0.5) * 22
    let inclusions = 0
    for (let index = 0; index < 5; index += 1) {
      const centerX = unitNoise(seed, index, 41, 2) * tileSize
      const centerY = unitNoise(seed, index, 73, 3) * tileSize
      const radius = 2.5 + unitNoise(seed, index, 19, 4) * 5
      const distance = Math.hypot(x - centerX, y - centerY)
      if (distance < radius) {
        const direction = index % 2 === 0 ? 1 : -1
        inclusions += direction * (10 + unitNoise(seed, index, 29, 5) * 16) * (1 - distance / radius)
      }
    }
    const fleckNoise = unitNoise(seed, x, y, 6)
    const fleck = fleckNoise > 0.97 ? 26 : fleckNoise < 0.025 ? -20 : 0
    return { amount: grain + coarse + inclusions + fleck, tint: [0, 0, 0] }
  }

  if (category === 'Glass') {
    const diagonal = (x + y + (seed % 13)) % 15
    const opposing = (x - y + 96 + (seed % 11)) % 19
    const facet = diagonal <= 1 ? 28 : opposing <= 1 ? -18 : 0
    const edge = x < 2 || y < 2 || x > tileSize - 3 || y > tileSize - 3 ? 18 : 0
    return { amount: grain * 0.45 + facet + edge, tint: [-2, 3, 8] }
  }

  if (category === 'Ceramic') {
    const centerX = 18 + (seed % 13)
    const centerY = 18 + ((seed >>> 8) % 13)
    const distance = Math.hypot(x - centerX, y - centerY)
    const glaze = Math.sin(distance * 0.72 + (seed % 7)) * 9
    const speckle = unitNoise(seed, x, y, 3) > 0.94 ? -20 : 0
    const highlight = ((x - centerX) ** 2) + ((y - centerY) ** 2) < 45 ? 10 : 0
    return { amount: grain * 0.55 + glaze + speckle + highlight, tint: [3, 0, 2] }
  }

  const brush = y % 6 === 0 ? 17 : y % 6 === 1 ? -9 : 0
  const sheenCenter = 1 - Math.min(1, Math.abs(x - (tileSize - 1) / 2) / (tileSize / 2))
  const pin = (x + (seed % 9)) % 17 === 0 && (y + ((seed >>> 8) % 7)) % 13 === 0 ? 20 : 0
  return { amount: grain * 0.35 + brush + sheenCenter * 12 + pin, tint: [2, 2, 3] }
}

function createTexture(material) {
  const seed = seedFor(material.id)
  const base = parseHex(material.colorHex)
  const pixels = Buffer.alloc(tileSize * tileSize * 4)

  for (let y = 0; y < tileSize; y += 1) {
    for (let x = 0; x < tileSize; x += 1) {
      const { amount, tint } = textureAdjustment(material.category, seed, x, y)
      const offset = (y * tileSize + x) * 4
      pixels[offset] = clamp(base[0] + amount + tint[0])
      pixels[offset + 1] = clamp(base[1] + amount + tint[1])
      pixels[offset + 2] = clamp(base[2] + amount + tint[2])
      pixels[offset + 3] = 255
    }
  }

  return sharp(pixels, { raw: { width: tileSize, height: tileSize, channels: 4 } })
    .png({ compressionLevel: 9, palette: false })
    .toBuffer()
}

await mkdir(outputDir, { recursive: true })
await mkdir(dirname(generatedPath), { recursive: true })
for (const entry of await readdir(outputDir).catch(() => [])) {
  if (entry.endsWith('.png') || entry === 'catalog.json') await unlink(join(outputDir, entry))
}

const materials = []
for (const definition of materialPalette) {
  const texture = await createTexture(definition)
  const textureSha256 = createHash('sha256').update(texture).digest('hex')
  await writeFile(join(outputDir, `${definition.id}.png`), texture)
  materials.push({
    ...definition,
    swatchUrl: `/materials/${definition.id}.png`,
    width: tileSize,
    height: tileSize,
    textureSha256,
  })
}

const generated = `// Generated by scripts/generate-materials.mjs from the original procedural texture generator. Do not edit by hand.\n` +
`export type MaterialCategory = 'Stone' | 'Glass' | 'Ceramic' | 'Metal'\n\n` +
`export interface Material {\n  id: string\n  name: string\n  category: MaterialCategory\n  colorHex: string\n  swatchUrl: string\n  width: 48\n  height: 48\n  textureSha256: string\n}\n\n` +
`export const TEXTURE_GENERATOR = ${JSON.stringify(generator, null, 2)} as const\n\n` +
`export const MATERIALS: Material[] = ${JSON.stringify(materials, null, 2)} as Material[]\n`

await writeFile(generatedPath, generated)
await writeFile(join(outputDir, 'catalog.json'), `${JSON.stringify({ generator, materials }, null, 2)}\n`)
console.log(`Generated ${materials.length} deterministic original procedural textures (${tileSize}x${tileSize}, generator v${generator.version}).`)
